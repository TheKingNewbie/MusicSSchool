package Beans;

/**
 *
 * @author GSantiago
 */
public class MusicasBeans {

    private long idMusicas;
    private String estilo;
    private String cantor;
    private String hiperlink;
    private String genero;
    private String nomeMusica;

    public String getEstilo() {
        return estilo;
    }

    public void setEstilo(String estilo) {
        this.estilo = estilo;
    }

    public String getCantor() {
        return cantor;
    }

    public void setCantor(String cantor) {
        this.cantor = cantor;
    }

    public String getHiperlink() {
        return hiperlink;
    }

    public void setHiperlink(String hiperlink) {
        this.hiperlink = hiperlink;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getNomeMusica() {
        return nomeMusica;
    }

    public void setNomeMusica(String nomeMusica) {
        this.nomeMusica = nomeMusica;
    }

    public long getIdMusicas() {
        return idMusicas;
    }

    public void setIdMusicas(long idMusicas) {
        this.idMusicas = idMusicas;
    }

}
