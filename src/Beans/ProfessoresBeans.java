package Beans;

/**
 *
 * @author Jeels
 */
public class ProfessoresBeans {
    
    private long cod_Professor;
    private String nomeProfessor;
    private String cpf;
    private String dataNascimento;
    private String endereco;
    private String bairro;
    private String telefone;
    private String email;
    private String Intrumentos;

    public long getCod_Professor() {
        return cod_Professor;
    }

    public void setCod_Professor(long cod_Professor) {
        this.cod_Professor = cod_Professor;
    }

    public String getNomeProfessor() {
        return nomeProfessor;
    }

    public void setNomeProfessor(String nomeProfessor) {
        this.nomeProfessor = nomeProfessor;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(String dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getIntrumentos() {
        return Intrumentos;
    }

    public void setIntrumentos(String Intrumentos) {
        this.Intrumentos = Intrumentos;
    }

   
}
